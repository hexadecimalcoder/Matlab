clear all;
clc;
n=input('Enter the length of the sinusoid: ');
f1=input('Enter freq of sinusoid: ');
f2=input('Enter freq of sinusoid: ');
t=0:0.5:(n-1);
x1=sin(2*pi*f1*t);
x2=sin(2*pi*f2*t);
subplot(2,3,1);
stem(t,x1);
subplot(2,3,2);
stem(t,x2);
x3=x1.*x2;
subplot(2,3,4);
stem(t,(x3));
x4=x1+x2;
subplot(2,3,5);
stem(t,(x4));
x5=x1-x2;
subplot(2,3,6);
stem(t,(x5));